
package Model;

public class dataCore {
    String core;
    String core_awal;
    String user;
    String hasil_ukur;
    String redaman;
    String core_lawan;
    String keterangan;

    public dataCore(String core, String core_awal, String user, String hasil_ukur, String redaman, String core_lawan, String keterangan) {
        this.core = core;
        this.core_awal = core_awal;
        this.user = user;
        this.hasil_ukur = hasil_ukur;
        this.redaman = redaman;
        this.core_lawan = core_lawan;
        this.keterangan = keterangan;
    }

    public String getCore() {
        return core;
    }

    public void setCore(String core) {
        this.core = core;
    }

    public String getCore_awal() {
        return core_awal;
    }

    public void setCore_awal(String core_awal) {
        this.core_awal = core_awal;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getHasil_ukur() {
        return hasil_ukur;
    }

    public void setHasil_ukur(String hasil_ukur) {
        this.hasil_ukur = hasil_ukur;
    }

    public String getRedaman() {
        return redaman;
    }

    public void setRedaman(String redaman) {
        this.redaman = redaman;
    }

    public String getCore_lawan() {
        return core_lawan;
    }

    public void setCore_lawan(String core_lawan) {
        this.core_lawan = core_lawan;
    }

    public String getKeterangan() {
        return keterangan;
    }

    public void setKeterangan(String keterangan) {
        this.keterangan = keterangan;
    }
    
    
    
}
